# Percentual do salario com as despesas
# Para calcular o percentual do salário com as despesas, você precisa 
# primeiro calcular o total das despesas e o total do salário. 
# Em seguida, basta dividir o total das despesas pelo 
# total do salário e multiplicar o resultado por 100.
# A fórmula para calcular o percentual do salário com as despesas é a seguinte:
# Percentual = (Despesas / Salário) * 100

# Por exemplo, se você tem um salário 
# de R$ 1.000,00 e suas despesas totalizam R$ 500,00, o percentual do salário com as despesas é de 50%.

# Percentual = (500 / 1000) * 100
# Percentual = 0.5 * 100
# Percentual = 50

salario = 3450.45
despesas = 2456.2
percentual = (despesas/salario)*100
print('Salario: ' + str(salario))
print('Salario: ' + str(despesas))

print('Percentual do Salário com as despesas:' + str(percentual))
